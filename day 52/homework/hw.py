# рЃЌрЃБ рЃАрЃўрЃбрЃДрЃЋрЃљ рЃерЃћрЃўрЃфрЃљрЃЋрЃА рЃЌрЃБрЃюрЃЊрЃљрЃф рЃћрЃарЃЌ uppercase рЃљрЃАрЃЮрЃА, рЃћрЃА рЃАрЃўрЃбрЃДрЃЋрЃљ рЃљрЃЏрЃЮрЃерЃљрЃџрЃћрЃЌ рЃАрЃўрЃўрЃЊрЃљрЃю. рЃЉрЃЮрЃџрЃЮрЃА рЃЊрЃљрЃърЃарЃўрЃюрЃбрЃћрЃЌ рЃЏрЃўрЃдрЃћрЃЉрЃБрЃџрЃў рЃАрЃўрЃљ. (рЃљрЃа рЃерЃћрЃЦрЃЏрЃюрЃљрЃЌ рЃљрЃ«рЃљрЃџрЃў рЃАрЃўрЃљ, рЃўрЃЏрЃБрЃерЃљрЃЋрЃћрЃЌ рЃърЃўрЃарЃЋрЃћрЃџ рЃАрЃўрЃбрЃДрЃЋрЃћрЃЉрЃўрЃА рЃАрЃўрЃљрЃерЃў) рЃњрЃљрЃЏрЃЮрЃўрЃДрЃћрЃюрЃћрЃЌ while рЃфрЃўрЃЎрЃџрЃў.

names = ["tbilisi", "Batumi", "Qutaisi", "Zugdidi", "poti", "senaki"]

i = 0

while i < len(names):
    word = names[i]
    if word == word.lower():
        names[i] = word.upper()
        i += 1
    else:
        names.pop(i)

print(names)

